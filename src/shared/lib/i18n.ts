// Detectar idioma inicial desde localStorage de Zustand
const getInitialLanguage = () => {
  try {
    const settings = localStorage.getItem('gymlog-settings');
    if (settings) {
      const parsed = JSON.parse(settings);
      return parsed.state?.language || 'es';
    }
  } catch {
    return 'es';
  }
  return 'es';
};

